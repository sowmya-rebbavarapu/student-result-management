    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        String rollno = jTextField1.getText();
        String oops = jTextField2.getText();
        String dbms = jTextField3.getText();
        String cosm = jTextField4.getText();
        String edc = jTextField5.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sowmya", "root", "root");
            Statement st = con.createStatement();
            ResultSet re = st.executeQuery("select * from student where rollNo='" + rollno + "'");
            if (re.next()) {
                st.executeUpdate("insert into result (rollno, oops, dbms, cosm, edc) values('" + rollno + "','" + oops + "','" + dbms + "','" + cosm + "','" + edc + "')");
                JOptionPane.showMessageDialog(null, "Successfully updated");
                setVisible(false);
                new insertnewstudent().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Roll number doesn't exist");
                setVisible(false);
                new insertnewstudent().setVisible(true);
            }    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "connection error");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

